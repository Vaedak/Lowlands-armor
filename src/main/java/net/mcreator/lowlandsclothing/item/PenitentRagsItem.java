	@SubscribeEvent
	public static void registerArmorMaterial(RegisterEvent event) {
		event.register(Registries.ARMOR_MATERIAL, registerHelper -> {
			ArmorMaterial armorMaterial = new ArmorMaterial(Util.make(new EnumMap<>(ArmorItem.Type.class), map -> {
				map.put(ArmorItem.Type.BOOTS, 1);
				map.put(ArmorItem.Type.LEGGINGS, 1);
				map.put(ArmorItem.Type.CHESTPLATE, 1);
				map.put(ArmorItem.Type.HELMET, 1);
				map.put(ArmorItem.Type.BODY, 1);
			}), 9, DeferredHolder.create(Registries.SOUND_EVENT, ResourceLocation.parse("block.wool.hit")), () -> Ingredient.of(new ItemStack(Items.LEATHER)), List.of(new ArmorMaterial.Layer(ResourceLocation.parse("lowlands_clothing:penitant"))), 0f,
					0f);
			registerHelper.register(ResourceLocation.parse("lowlands_clothing:penitent_rags"), armorMaterial);
			ARMOR_MATERIAL = BuiltInRegistries.ARMOR_MATERIAL.wrapAsHolder(armorMaterial);
		});
	}

